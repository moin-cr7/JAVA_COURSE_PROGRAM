package programm;

public class IndexOfCharacterInString {
	public static void main(String[] args) {
		String s="Bhoomiokoa";
		System.out.println(s.indexOf('o'));              //indexOf(character, fromIndex)
		System.out.println(s.indexOf('o',3));
		System.out.println(s.indexOf('o',4));
		System.out.println(s.indexOf('o',7));

	}
}
